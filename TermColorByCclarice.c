void printrainbow(int r, int g, int b)
{
	int i;

	i = 0;
	if (r % 11 == 1 || g % 11 == 1 || b % 11 == 1)
		catint++;
	if (catint > 9)
		catint = 0;
	printf("\033[0;1;38:2:%d:%d:%dm", r, g, b);
	fflush(stdout);
	while (i < 26)
	{
		printf("%s\n", cats[catint][i]);
		fflush(stdout);
		i++;
	}
	write(1, "\033[26A", 5);
}

